
namespace Practice
{
    public class Calculation
    {
        public static int Add(int x, int y)
        {
            return x + y;
        }

        public static int Sub(int x, int y)
        {
            return x - y;
        } 
        
        public static int Mul(int x, int y)
        {
            return x * y;
        }

        public static double Div(double x, double y)
        {
            return x / y;
        }
    }
}
